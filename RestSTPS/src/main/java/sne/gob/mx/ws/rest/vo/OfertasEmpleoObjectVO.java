package sne.gob.mx.ws.rest.vo;

public class OfertasEmpleoObjectVO {
	
	private String idOfertasEmpleo;
	private String idEmpleador;
	private String idVacante;
	private String fechaInicio;
	private String fechaFin;
	private String descripcion;
	private String estatus;
	
	
	
	public String getIdOfertasEmpleo() {
		return idOfertasEmpleo;
	}
	public void setIdOfertasEmpleo(String idOfertasEmpleo) {
		this.idOfertasEmpleo = idOfertasEmpleo;
	}
	public String getIdEmpleador() {
		return idEmpleador;
	}
	public void setIdEmpleador(String idEmpleador) {
		this.idEmpleador = idEmpleador;
	}
	public String getIdVacante() {
		return idVacante;
	}
	public void setIdVacante(String idVacante) {
		this.idVacante = idVacante;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	
	
}
