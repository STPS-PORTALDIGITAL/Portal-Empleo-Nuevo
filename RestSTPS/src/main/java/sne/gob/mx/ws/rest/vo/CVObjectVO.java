package sne.gob.mx.ws.rest.vo;

public class CVObjectVO {

	private String idCV;
	private String idBuscadorTrabajo;
	private String idDisponibilidadMovilidad;
	private String idEscolaridad;
	private String idExpectativaLaboral;
	private String estatus;
	
	
	public String getIdCV() {
		return idCV;
	}
	public void setIdCV(String idCV) {
		this.idCV = idCV;
	}
	public String getIdBuscadorTrabajo() {
		return idBuscadorTrabajo;
	}
	public void setIdBuscadorTrabajo(String idBuscadorTrabajo) {
		this.idBuscadorTrabajo = idBuscadorTrabajo;
	}
	public String getIdDisponibilidadMovilidad() {
		return idDisponibilidadMovilidad;
	}
	public void setIdDisponibilidadMovilidad(String idDisponibilidadMovilidad) {
		this.idDisponibilidadMovilidad = idDisponibilidadMovilidad;
	}
	public String getIdEscolaridad() {
		return idEscolaridad;
	}
	public void setIdEscolaridad(String idEscolaridad) {
		this.idEscolaridad = idEscolaridad;
	}
	public String getIdExpectativaLaboral() {
		return idExpectativaLaboral;
	}
	public void setIdExpectativaLaboral(String idExpectativaLaboral) {
		this.idExpectativaLaboral = idExpectativaLaboral;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	
	
}
