package sne.gob.mx.ws.rest.vo;

public class EmpleadorObjectVO {
	private String idEmpleador;
	private String idDatosContacto;
	private String descripcionEmpresa;
	private String nombre;
	private String rfc;
	private String idUsuario;
	private String idDomicilio;
	private String idSector;
	private String idTipoEmpresa;
	private String estatus;
	
	
	public String getIdEmpleador() {
		return idEmpleador;
	}
	public void setIdEmpleador(String idEmpleador) {
		this.idEmpleador = idEmpleador;
	}
	public String getIdDatosContacto() {
		return idDatosContacto;
	}
	public void setIdDatosContacto(String idDatosContacto) {
		this.idDatosContacto = idDatosContacto;
	}
	public String getDescripcionEmpresa() {
		return descripcionEmpresa;
	}
	public void setDescripcionEmpresa(String descripcionEmpresa) {
		this.descripcionEmpresa = descripcionEmpresa;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getRfc() {
		return rfc;
	}
	public void setRfc(String rfc) {
		this.rfc = rfc;
	}
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getIdDomicilio() {
		return idDomicilio;
	}
	public void setIdDomicilio(String idDomicilio) {
		this.idDomicilio = idDomicilio;
	}
	public String getIdSector() {
		return idSector;
	}
	public void setIdSector(String idSector) {
		this.idSector = idSector;
	}
	public String getIdTipoEmpresa() {
		return idTipoEmpresa;
	}
	public void setIdTipoEmpresa(String idTipoEmpresa) {
		this.idTipoEmpresa = idTipoEmpresa;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	
	
	

}
