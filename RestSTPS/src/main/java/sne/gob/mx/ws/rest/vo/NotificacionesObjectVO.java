package sne.gob.mx.ws.rest.vo;

public class NotificacionesObjectVO {
	
	private String idNotificaciones;
	private String idRolesResponsabilidades;
	private String fecha;
	private String from;
	private String to;
	private String subject;
	private String message;
	private String estatus;
	
	
	
	public String getIdNotificaciones() {
		return idNotificaciones;
	}
	public void setIdNotificaciones(String idNotificaciones) {
		this.idNotificaciones = idNotificaciones;
	}
	public String getIdRolesResponsabilidades() {
		return idRolesResponsabilidades;
	}
	public void setIdRolesResponsabilidades(String idRolesResponsabilidades) {
		this.idRolesResponsabilidades = idRolesResponsabilidades;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	
	
	
	
}
